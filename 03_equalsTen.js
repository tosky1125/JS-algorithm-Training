//Write a function called "equalsTen". ("equalsTen" 함수를 작성하세요.)

//Given a number, "equalsTen" returns whether or not the given number is 10. ("숫자가 주어졌을때, "equalsTen" 함수는 숫자가 10과 같은지 반환합니다.)

function equalsTen(num) {
    if(num === 10) {
        return true;
    } else {
        return false;
    }
}
